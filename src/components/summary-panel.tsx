// ── Types ─────────────────────────────────────────────────────────────────────

interface Props {
  syntaxIScore: number
  hasLeftMain: boolean
  syntaxIIResult: SyntaxIIResult | null
  euroScoreResult: EuroScoreIIResult | null
  eftResult: EFTResult | null
  patientAge?: number
  patientGender?: 'male' | 'female'
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function syntaxICategory(score: number): { label: string; color: string } {
  if (score <= 22) return { label: 'Low', color: 'text-emerald-600' }
  if (score <= 32) return { label: 'Intermediate', color: 'text-amber-600' }
  return { label: 'High', color: 'text-red-600' }
}

function euroRiskLabel(mort: number): { label: string; color: string } {
  if (mort < 2) return { label: 'Low', color: 'text-emerald-600' }
  if (mort < 5) return { label: 'Moderate', color: 'text-amber-600' }
  return { label: 'High', color: 'text-red-600' }
}

const FRAILTY_LABELS: Record<FrailtyClass, { label: string; color: string }> = {
  robust:   { label: 'Robust', color: 'text-emerald-600' },
  prefrail: { label: 'Pre-Frail', color: 'text-amber-600' },
  frail:    { label: 'Frail', color: 'text-red-600' },
}

function formatPct(n: number): string { return n.toFixed(1) + '%' }
function formatInt(n: number): string { return Math.round(n).toString() }

// ── Export ─────────────────────────────────────────────────────────────────────

function exportToText(props: Props): string {
  const { syntaxIScore, hasLeftMain, syntaxIIResult, euroScoreResult, eftResult, patientAge, patientGender } = props
  const cat = syntaxICategory(syntaxIScore)
  const lines: string[] = []

  lines.push('═══════════════════════════════════════════')
  lines.push('      CORONARY RISK ASSESSMENT SUMMARY')
  lines.push('═══════════════════════════════════════════')
  lines.push('')

  // Patient
  if (patientAge || patientGender) {
    lines.push('PATIENT')
    if (patientAge) lines.push(`  Age:    ${patientAge} years`)
    if (patientGender) lines.push(`  Gender: ${patientGender === 'male' ? 'Male' : 'Female'}`)
    lines.push('')
  }

  // SYNTAX I
  lines.push('─── SYNTAX SCORE I (Anatomical Complexity) ───')
  lines.push(`  Score:       ${formatInt(syntaxIScore)}`)
  lines.push(`  Category:    ${cat.label} (${syntaxIScore <= 22 ? '≤22' : syntaxIScore <= 32 ? '23-32' : '≥33'})`)
  lines.push(`  Left Main:   ${hasLeftMain ? 'Yes' : 'No'}`)
  lines.push('')

  // SYNTAX II
  if (syntaxIIResult) {
    lines.push('─── SYNTAX SCORE II ───')
    lines.push(`  PCI:  SS2 = ${formatInt(syntaxIIResult.ss2PCI)}, 4-yr mortality = ${formatPct(syntaxIIResult.mortalityPCI)}`)
    lines.push(`  CABG: SS2 = ${formatInt(syntaxIIResult.ss2CABG)}, 4-yr mortality = ${formatPct(syntaxIIResult.mortalityCABG)}`)
    lines.push('')
  }

  // EuroSCORE II
  if (euroScoreResult) {
    const er = euroRiskLabel(euroScoreResult.predictedMortality)
    lines.push('─── EuroSCORE II (Surgical Risk) ───')
    lines.push(`  Predicted in-hospital mortality: ${formatPct(euroScoreResult.predictedMortality)}`)
    lines.push(`  Risk category: ${er.label}`)
    lines.push('')
  }

  // EFT
  if (eftResult) {
    const fl = FRAILTY_LABELS[eftResult.frailtyClass]
    lines.push('─── ESSENTIAL FRAILTY TOOLSET ───')
    lines.push(`  EFT Score:      ${eftResult.score}/5`)
    lines.push(`  Classification: ${fl.label}`)
    lines.push(`  CABG 1-yr mortality: ${eftResult.mortality1yr.cabg}%`)
    lines.push(`  CABG 5-yr mortality: ${eftResult.mortality5yr.cabg}%`)
    lines.push('')
  }

  // Overall
  lines.push('─── INTEGRATED ASSESSMENT ───')
  if (euroScoreResult) {
    lines.push(`  Surgical risk: ${formatPct(euroScoreResult.predictedMortality)} (EuroSCORE II)`)
  }
  if (eftResult) {
    lines.push(`  Frailty: ${FRAILTY_LABELS[eftResult.frailtyClass].label} (EFT ${eftResult.score}/5)`)
  }
  lines.push('')
  lines.push('═══════════════════════════════════════════')
  lines.push('Generated by SYNTAX Score Calculator')
  lines.push(`Date: ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`)
  lines.push('')
  lines.push('References:')
  lines.push('• SYNTAX I: Sianos et al., EuroIntervention 2005')
  lines.push('• SYNTAX II: Farooq et al., Lancet 2013')
  lines.push('• EuroSCORE II: Nashef et al., EJCTS 2012')
  lines.push('• EFT: Solomon et al., JAHA 2021; Afilalo et al., JACC 2017')

  return lines.join('\n')
}

