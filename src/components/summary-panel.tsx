// ── Component ─────────────────────────────────────────────────────────────────

export function SummaryPanel(props: Props) {
  const { syntaxIScore, hasLeftMain, syntaxIIResult, euroScoreResult, eftResult, patientAge, patientGender } = props
  const cat = syntaxICategory(syntaxIScore)

  const handleCopy = () => {
    const text = exportToText(props)
    navigator.clipboard.writeText(text)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  const handlePrint = () => {
    const html = generateHTML(props)
    const win = window.open('', '_blank')
    if (!win) return
    win.document.write(html)
    win.document.close()
    win.print()
  }

  const handleShareHTML = () => {
    const html = generateHTML(props)
    const blob = new Blob([html], { type: 'text/html' })
    const url = URL.createObjectURL(blob)

    // Try Web Share API first (mobile)
    if (navigator.share && navigator.canShare) {
      const file = new File([blob], 'coronary-risk-summary.html', { type: 'text/html' })
      if (navigator.canShare({ files: [file] })) {
        navigator.share({ files: [file], title: 'Coronary Risk Assessment Summary' }).catch(() => {
          // Fallback: download
          downloadBlob(blob)
        })
        return
      }
    }
    // Fallback: download file
    downloadBlob(blob)
  }

  const downloadBlob = (blob: Blob) => {
    const url = URL.createObjectURL(blob)
    const a = document.createElement('a')
    a.href = url
    a.download = 'coronary-risk-summary.html'
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
  }

  const [copied, setCopied] = useState(false)

  return (
    <div className="rounded-xl border-2 border-gray-300 bg-white overflow-hidden shadow-md">
      {/* Header */}
      <div className="px-4 py-3 bg-gray-800 flex items-center justify-between">
        <div>
          <h2 className="text-sm font-bold text-white">
            📋 Risk Assessment Summary
          </h2>
          <p className="text-xs text-gray-400 mt-0.5">Combined results from all 4 scores</p>
        </div>
        <div className="flex gap-2">
          <button onClick={handleCopy}
            className="px-3 py-1.5 text-xs font-semibold bg-gray-700 hover:bg-gray-600 text-gray-200 rounded-lg transition-colors">
            {copied ? '✓ Copied' : '📋 Copy'}
          </button>
          <button onClick={handleShareHTML}
            className="px-3 py-1.5 text-xs font-semibold bg-emerald-600 hover:bg-emerald-500 text-white rounded-lg transition-colors">
            📤 HTML
          </button>
          <button onClick={handlePrint}
            className="px-3 py-1.5 text-xs font-semibold bg-blue-600 hover:bg-blue-500 text-white rounded-lg transition-colors">
            🖨 Print
          </button>
        </div>
      </div>

      <div className="p-4 space-y-3">
        {/* Patient */}
        {(patientAge || patientGender) && (
          <div className="flex items-center gap-4 text-xs text-gray-500 pb-2 border-b border-gray-100">
            {patientAge && <span>Age: <strong className="text-gray-800">{patientAge}</strong></span>}
            {patientGender && <span>Gender: <strong className="text-gray-800 capitalize">{patientGender}</strong></span>}
          </div>
        )}

        {/* Grid of 4 scores */}
        <div className="grid grid-cols-2 lg:grid-cols-4 gap-3">
          {/* SYNTAX I */}
          <div className="p-3 rounded-xl bg-blue-50 border border-blue-100">
            <p className="text-[10px] font-bold text-blue-500 uppercase tracking-wider mb-1">SYNTAX I</p>
            <p className="text-2xl font-bold text-blue-800 tabular-nums">{formatInt(syntaxIScore)}</p>
            <p className={`text-xs font-semibold ${cat.color}`}>{cat.label} complexity</p>
            <p className="text-[10px] text-gray-400 mt-0.5">LM: {hasLeftMain ? 'Yes' : 'No'}</p>
          </div>

          {/* SYNTAX II */}
          <div className="p-3 rounded-xl bg-indigo-50 border border-indigo-100">
            <p className="text-[10px] font-bold text-indigo-500 uppercase tracking-wider mb-1">SYNTAX II</p>
            {syntaxIIResult ? (
              <>
                <div className="space-y-0.5 mt-0.5">
                  <p className="text-[11px] text-gray-600">PCI {formatPct(syntaxIIResult.mortalityPCI)}</p>
                  <p className="text-[11px] text-gray-600">CABG {formatPct(syntaxIIResult.mortalityCABG)}</p>
                </div>
                <p className="text-[10px] text-gray-400 mt-1">4-yr mortality</p>
              </>
            ) : (
              <p className="text-xs text-gray-400 mt-1">Enter clinical data</p>
            )}
          </div>

          {/* EuroSCORE II */}
          <div className="p-3 rounded-xl bg-rose-50 border border-rose-100">
            <p className="text-[10px] font-bold text-rose-500 uppercase tracking-wider mb-1">EuroSCORE II</p>
            {euroScoreResult ? (
              <>
                <p className="text-2xl font-bold tabular-nums"
                  style={{ color: euroScoreResult.riskCategory === 'low' ? '#059669' : euroScoreResult.riskCategory === 'moderate' ? '#d97706' : '#dc2626' }}>
                  {formatPct(euroScoreResult.predictedMortality)}
                </p>
                <p className={`text-xs font-semibold ${euroRiskLabel(euroScoreResult.predictedMortality).color}`}>
                  {euroRiskLabel(euroScoreResult.predictedMortality).label} risk
                </p>
                <p className="text-[10px] text-gray-400">In-hospital mortality</p>
              </>
            ) : (
              <p className="text-xs text-gray-400 mt-1">Enter clinical data</p>
            )}
          </div>

          {/* EFT */}
          <div className="p-3 rounded-xl bg-teal-50 border border-teal-100">
            <p className="text-[10px] font-bold text-teal-500 uppercase tracking-wider mb-1">Frailty (EFT)</p>
            {eftResult ? (
              <>
                <p className="text-2xl font-bold text-teal-800 tabular-nums">{eftResult.score}/5</p>
                <p className={`text-xs font-semibold ${FRAILTY_LABELS[eftResult.frailtyClass].color}`}>
                  {FRAILTY_LABELS[eftResult.frailtyClass].label}
                </p>
                <p className="text-[10px] text-gray-400">CABG 1yr: {eftResult.mortality1yr.cabg}%</p>
              </>
            ) : (
              <p className="text-xs text-gray-400 mt-1">Enter frailty data</p>
            )}
          </div>
        </div>


      </div>
    </div>
  )
}
